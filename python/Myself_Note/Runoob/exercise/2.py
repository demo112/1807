li = int(input('请输入当月利润__万元'))
arr = [100, 60, 40, 20, 10, 0]
rat = [0.01, 0.015, 0.03, 0.05, 0.075, 0.1]
money = 0
for x in range(6):
    if li > arr[x]:
        money += rat[x] * (li - arr[x])
        li = arr[x]
print(money)
